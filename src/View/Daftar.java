    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("".equals(jTextField1.getText()) || "".equals(jTextField2.getText())) {
            JOptionPane.showMessageDialog(null, "Nama dan Password tidak boleh Kosong");
            return;
        }
        try {
            User.SignUp(jTextField1.getText(), jTextField2.getText());
            JOptionPane.showMessageDialog(null, "Berhasil Terdaftar");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Tidak Berhasil Mendaftar");
            Utils.Logger.Error("Gagal Dalam mendaftar");
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

